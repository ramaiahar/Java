package Concepts;

public class MathPreDefinedClass {

	public static void main(String[] args) {
		System.out.println(Math.abs(-12));
		System.out.println(Math.cbrt(9));
		System.out.println(Math.ceil(9.9));
		System.out.println(Math.cos(90));
		System.out.println(Math.floor(9.9));
		System.out.println(Math.log(1000));
		System.out.println(Math.log10(1000));
		System.out.println(Math.max(5, 6));
		System.out.println(Math.max(5.5, 6.5));
		System.out.println(Math.min(5, 6));
		System.out.println(Math.min(5.2, 6.2));
		System.out.println(Math.pow(2, 8));
		System.out.println(Math.random());
		System.out.println(Math.round(9.9));
		System.out.println(Math.sin(90));
		System.out.println(Math.sqrt(4));
		System.out.println(Math.tan(90));
	}
}